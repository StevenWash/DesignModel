package com.huaxin;

public class GarageDoor {
    public void up(){
        System.out.println("门打开了");
    }

    public void down(){
        System.out.println("门关上了");
    }

    public void stop(){
        System.out.println("门停住了");
    }

    public void lighton(){
        System.out.println("车库的灯打开了");
    }

    public void lightoff(){
        System.out.println("车库的灯关上了");
    }
}
